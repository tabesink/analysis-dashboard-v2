/**
 * Script to generate settings.ts from settings.yaml
 * This runs at build time to automatically sync YAML config with TypeScript code
 */

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const SETTINGS_YAML_PATH = path.join(__dirname, '..', 'settings.yaml');
const SETTINGS_TS_PATH = path.join(__dirname, '..', 'src', 'config', 'settings.ts');

try {
  // Read and parse YAML file
  const yamlContent = fs.readFileSync(SETTINGS_YAML_PATH, 'utf8');
  const settings = yaml.load(yamlContent);

  // Generate TypeScript file content
  const tsContent = `/**
 * Plot Settings
 * 
 * AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
 * This file is automatically generated from settings.yaml by scripts/generate-settings.js
 * 
 * To modify settings, edit client/settings.yaml and run: npm run generate:settings
 * Or it will be automatically regenerated on next build/dev
 */

// Plot dimensions
export const PLOT_WIDTH = ${settings.plot.width};
export const PLOT_HEIGHT = ${settings.plot.height};

// Plot configuration interface
export interface PlotConfig {
  title: string;
  x_label: string;
  y_label: string;
}

// Default plot keys for the grid (these come from channel_map)
// Dictionary mapping plot keys to configuration (title and axis labels)
export const DEFAULT_PLOT_KEYS: Record<string, PlotConfig> = {
${Object.entries(settings.default_plot_keys).map(([key, config]) => {
  const title = config.title || '';
  const xLabel = config.x_label || '';
  const yLabel = config.y_label || '';
  return `  '${key}': {\n    title: '${title}',\n    x_label: '${xLabel}',\n    y_label: '${yLabel}',\n  },`;
}).join('\n')}
};

// Array of plot keys (for API calls and iteration)
export const DEFAULT_PLOT_KEYS_ARRAY = Object.keys(DEFAULT_PLOT_KEYS) as readonly string[];

// Helper function to get display title for a plot key
export function getPlotDisplayTitle(plotKey: string): string {
  return DEFAULT_PLOT_KEYS[plotKey]?.title || plotKey.replace(/_/g, ' ').replace(/\\b\\w/g, (c) => c.toUpperCase());
}

// Helper function to get X axis label for a plot key
export function getPlotXLabel(plotKey: string): string {
  return DEFAULT_PLOT_KEYS[plotKey]?.x_label || 'X';
}

// Helper function to get Y axis label for a plot key
export function getPlotYLabel(plotKey: string): string {
  return DEFAULT_PLOT_KEYS[plotKey]?.y_label || 'Y';
}

// Helper function to get full plot configuration for a plot key
export function getPlotConfig(plotKey: string): PlotConfig | null {
  return DEFAULT_PLOT_KEYS[plotKey] || null;
}

// Opacity settings
export const DEFAULT_BASELINE_OPACITY = ${settings.opacity.default_baseline};
export const NEW_DATA_OPACITY = ${settings.opacity.new_data};

// Grid layout options
export const GRID_COLUMN_OPTIONS = [${settings.grid.column_options.join(', ')}] as const;
export const DEFAULT_GRID_COLUMNS = ${settings.grid.default_columns};

// Color palette for grouping (colorblind-friendly)
export const COLOR_PALETTE = [
${settings.color_palette.map(color => `  '${color}',`).join('\n')}
] as const;

// Max groups before showing warning
export const MAX_DISTINCT_GROUPS = ${settings.max_distinct_groups};

// Debounce timings (ms)
export const SESSION_SAVE_DEBOUNCE = ${settings.debounce.session_save};
export const FILTER_DEBOUNCE = ${settings.debounce.filter};
`;

  // Write TypeScript file
  fs.writeFileSync(SETTINGS_TS_PATH, tsContent, 'utf8');
  
  console.log('✓ Successfully generated settings.ts from settings.yaml');
} catch (error) {
  console.error('✗ Error generating settings.ts:', error.message);
  process.exit(1);
}

